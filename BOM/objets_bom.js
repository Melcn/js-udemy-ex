// Window : L'objet Window a pour fonction d'englober l'ensemble des fonctions, et autres objets de JavaScript. C'est de lui que tout part.

// Navigator : L'objet Navigator a pour fonction de fournir tout un tas d'informations sur le navigateur de nos utilisateurs. Pour certaines de ces informations, vous devrez demander une permission grâce à une boîte de dialogue.

// History : L'objet History a pour fonction de manipuler l'historique de nos utilisateurs.

// Location : L'objet Location a pour fonction de nous donner des informations relatives aux adresses, pour, par exemple, rediriger l'utilisateur.

// Screen : L'objet Screen a pour fonction de nous donner des informations relatives aux écrans.